    private void btnEliminaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminaActionPerformed
       DefaultTableModel model = (DefaultTableModel) tblDatos.getModel();
       int a =tblDatos.getSelectedRow();
       if (a<0) {
           JOptionPane.showConfirmDialog(null,"Debes seleccionaruna fila de la tabla");
           } else{
           int confirmar = JOptionPane.showConfirmDialog(null, "Estas seguro de eliminar el registro");
           if (JOptionPane.OK_OPTION==confirmar){
               model.removeRow(a);
               JOptionPane.showMessageDialog(null, "El registro ha sido eleiminado");
           }
       }
    }//GEN-LAST:event_btnEliminaActionPerformed

