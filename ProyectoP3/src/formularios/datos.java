    private void txtNombreKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNombreKeyTyped
        char c = evt.getKeyChar(); //llamamos el evento
        if (Character.isDigit(c)) {//comparamos si ingresamos un digito
            evt.consume(); //evitar que se capture el digito
            JOptionPane.showMessageDialog(this,"Solo se permiten letras en este campo","Advertencia",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_txtNombreKeyTyped

    private void txtSemestreKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSemestreKeyTyped
        char c = evt.getKeyChar(); //llamamos el evento
        if (Character.isLetter(c)) {//comparamos si ingresamos una letra
            evt.consume(); //evitar que se capture el digito
            JOptionPane.showMessageDialog(this,"Solo se permiten n√∫meros en este campo","Advertencia",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_txtSemestreKeyTyped

    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        //Agregamos los valores de los controles a la tabla
        DefaultTableModel modelot = (DefaultTableModel)tblDatos.getModel();
        Object [] fila = new Object [6];
        fila [0] = txtNombre.getText();
        fila [1] = txtSemestre.getText();
        fila [2] = txtCorreo.getText();
        fila [3] = cmbSexo.getSelectedItem();
        fila [4] = cmbEdad.getSelectedItem();
        fila [5] = txtDir.getText();
        modelot.addRow(fila);
        tblDatos.setModel(modelot);
        //limpiamos controles depues de agregar el dato
        limpiar();
        //deshabilitamos controles despues de agregar datos y limpiar
        deshabilitar();
        //deshabilitamos el boton guardar despues de agregar limpiar y deshabilitar datos
        btnAgregar.setEnabled(false);
    }//GEN-LAST:event_btnAgregarActionPerformed

